    private void showUsers(List<UserModel> list) throws SQLException{
        
        DefaultTableModel users = (DefaultTableModel) jTableMaintainers.getModel();
        Object column[] = new Object[9];
        
        for(int i=0;i<list.size();i++){
            column[0] = list.get(i).getUsername();
            column[1] = competence.getCommonSkills(skills, list.get(i).getUsername());
            column[2] = list.get(i).getSurname();
            column[3] = list.get(i).getSurname();
            column[4] = list.get(i).getSurname();
            column[5] = list.get(i).getSurname();
            column[6] = list.get(i).getSurname();
            column[7] = list.get(i).getSurname();
            column[8] = list.get(i).getSurname();
            
            users.addRow(column);
        }      
    }
    
